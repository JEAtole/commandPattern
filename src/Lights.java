public class Lights implements Device {

    public void on() {
        System.out.println("The lights are turned on.");
    }

    public void off() {
        System.out.println("The lights are turned off.");
    }

    public void up() {
        System.out.println("The lights are shining brighter.");
    }

    public void down() {
        System.out.println("The lights are dimmer.");
    }

}
